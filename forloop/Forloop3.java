package forloop;

public class Forloop3 {
	public static void main(String[] args) {
//To print 0 to 10 numbers 
	for(int i=0;i<=10;i++){
		System.out.println(i);
	
	}
	
// To print even numbers
	for(int i=0;i<=10;i++){
		System.out.println(i);
		i++;
	}
// To print odd numbers
	for(int i=1;i<=10;i++){
		System.out.println(i);
		i++;
	}
	}
}
